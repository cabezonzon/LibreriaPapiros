    private void mnenviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mnenviarActionPerformed
   
 
            DefaultTableModel tabladet = (DefaultTableModel) CompraMercaderia.jTableCompra.getModel();
            String[] dato = new String[5];

            int fila = jTableProducto.getSelectedRow();

            if (fila == -1) {
                JOptionPane.showMessageDialog(null, "No  ha seleccionado ningun registro");
            } else {
                String cod =  jTableProducto.getValueAt(fila, 0).toString();
                String nombre = jTableProducto.getValueAt(fila, 1).toString();
                String precio = jTableProducto.getValueAt(fila, 2).toString();
                String desc = jTableProducto.getValueAt(fila, 3).toString();
                String cant = JOptionPane.showInputDialog("ingrese cantidad");
                if ((cant.equals("")) || (cant.equals("0"))) {
                    JOptionPane.showMessageDialog(this, "Debe ingresar algun valor mayor que 0");
                } 
                       
                            dato[0] = cod;
                            dato[1] = nombre;
                            dato[2] = precio;
                            dato[3] = desc;
                           // dato[4] = stock;
                            dato[4] = cant;
                            
                            tabladet.addRow(dato);
                            CompraMercaderia.jTableCompra.setModel(tabladet);
                    }
        
    }//GEN-LAST:event_mnenviarActionPerformed

