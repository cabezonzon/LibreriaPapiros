    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       if (validar()) {

            repositorioUsuario rep = repositorioUsuario.getInstancia();
            String res = rep.acceder(txtusuario.getText(), txtcontra.getText());

            if (res.equals("admin")) {

                rep.guardarLogin(txtusuario.getText(), txtcontra.getText());
                this.dispose();
                MenuPrincipal aux = new MenuPrincipal();
                aux.setVisible(true);
                aux.pack();
            }
            else if (res.equals("vendedor")){
                rep.guardarLogin(txtusuario.getText(), txtcontra.getText());
                this.dispose();
                MenuV aux = new MenuV();
                aux.setVisible(true);
                aux.pack();
            }
            
            else  {
                JOptionPane.showMessageDialog(null, "Usuario o contraseña incorrectos");
            }
            
    }                     
    }//GEN-LAST:event_jButton1ActionPerformed

