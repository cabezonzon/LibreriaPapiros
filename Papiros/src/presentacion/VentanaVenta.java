    void numeros() {
        int j;
        String c = repositorioVenta.getInstancia().obtenerNumFactura();
        if (c == null) {
            txtFac.setText("00000001");
        } else {
            j = Integer.parseInt(c);
            GenerarNumero gen = new GenerarNumero();
            gen.generar(j);
            txtFac.setText(gen.serie());

        }

    }

