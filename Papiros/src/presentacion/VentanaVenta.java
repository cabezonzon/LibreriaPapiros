    private void buttonAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonAgregarActionPerformed
        Integer modelo = null;
        int c = 0;
        int j = 0;
        DefaultTableModel tabladet = (DefaultTableModel) VentanaVenta.jTableVenta.getModel();
        String[] dato = new String[5];
        String cod = (txtCodProducto.getText());
        String cant = (txtCant.getText());
        String nombre = txtProducto.getText();
        String precio = txtPrecioProducto.getText();
        if ((cant.equals("")) || (cant.equals("0")) || txtCodProducto.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Debe ingresar algun valor mayor que 0 o le falta ingresar el codigo de producto");
        } else {
            int canting = Integer.parseInt(cant);
            if ((repositorioStock.getInstancia().obtenerCantidad(modelo, Integer.parseInt(cod)) == null)) {
                JOptionPane.showMessageDialog(null, "No tiene stock registrado de este producto en el sistem");
            } else if (((repositorioStock.getInstancia().obtenerCantidad(modelo, Integer.parseInt(cod)) != null))) {

                int comp = Integer.parseInt(repositorioProducto.getInstancia().comparar(cod));
                if (canting > comp) {
                    JOptionPane.showMessageDialog(this, "Ud. no cuenta con el stock apropiado");

                } else {
                    for (int i = 0; i < VentanaVenta.jTableVenta.getRowCount(); i++) {
                        Object com = VentanaVenta.jTableVenta.getValueAt(i, 0);
                        if (cod.equals(com)) {
                            j = i;
                            VentanaVenta.jTableVenta.setValueAt(cant, i, 3);
                            c = c + 1;
                            calcular();
                        }
                    }
                    if (c == 0) {
                        dato[0] = cod;
                        dato[1] = nombre;
                        dato[2] = precio;
                        dato[3] = cant;
                        tabladet.addRow(dato);
                        VentanaVenta.jTableVenta.setModel(tabladet);
                        calcular();

                    }
                }

            }
        }

    }//GEN-LAST:event_buttonAgregarActionPerformed

