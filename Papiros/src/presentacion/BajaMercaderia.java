    private boolean validar() {
        try {
            if ("".equals(txtrazon.getText())) {
                JOptionPane.showMessageDialog(null, "El campo razón está vacio");
                return false;
            } else if ("".equals(dateControl.getDate().getTime())) {
                JOptionPane.showMessageDialog(null, "El campo fecha está vacío");
                return false;
            } else if ("".equals(txtcantidad.getText())) {
                JOptionPane.showMessageDialog(null, "El campo cantidad está vacío");
                return false;
            }
        } catch (HeadlessException e) {
            System.out.println(e.getMessage());

        }

        return true;
    }

