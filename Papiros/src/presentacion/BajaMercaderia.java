    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed

        if (validar()) {
            stock sto = new stock();
            movimientoStock ba = new movimientoStock();
            int fila = jTableProducto.getSelectedRow();
            repositorioStock rep = repositorioStock.getInstancia();
            repositorioMovimientoStock repp = repositorioMovimientoStock.getInstancia();
            ba.setFecha(new java.sql.Date(dateControl.getDate().getTime()));
            ba.setRazon(txtrazon.getText());
            if (fila >= 0) {
                DefaultTableModel tm = (DefaultTableModel) jTableProducto.getModel();
                sto.setIdProducto(Integer.parseInt((String) tm.getValueAt(jTableProducto.getSelectedRow(), 0)));
                ba.setIdProducto(Integer.parseInt((String) tm.getValueAt(jTableProducto.getSelectedRow(), 0)));
                Integer modelo = null;
                Integer obtenerCantidad = rep.obtenerCantidad(modelo, sto.getIdProducto());
                if (obtenerCantidad == null) {
                    obtenerCantidad = 0;
                }
                if (obtenerCantidad < Integer.parseInt(txtcantidad.getText())) {
                    JOptionPane.showMessageDialog(null, "No posee esa cantidad de mercaderia");
                } else {

                    sto.setCantidad(obtenerCantidad - (Integer.parseInt(txtcantidad.getText())));
                    ba.setCantidad(Integer.parseInt(txtcantidad.getText()));
                    rep.guardarStock(sto);
                    repp.guardarBaja(ba);
                    JOptionPane.showMessageDialog(null, "Producto descontado con éxito");
                }
                // mostrardatos1("");
                txtrazon.setText("");
                txtcantidad.setText("");
                mostrardatos("");
            }
    }//GEN-LAST:event_btnGuardarActionPerformed
    }
