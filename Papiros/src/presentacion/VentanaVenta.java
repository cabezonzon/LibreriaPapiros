    public void calcular() {
        String pre;
        String can;
        Integer modelo = null;
        double subtotal = 0;
        Integer precio;
        int cantidad;

        double iva = 0;
        double total = 0;
        double sub = 0;

        double iv = 0;

        double tot = 0;
        /*can=Integer.parseInt(cant);
         imp=pre*can;
         dato[4]=Float.toString(imp);*/

        for (int i = 0; i < jTableVenta.getRowCount(); i++) {
            Integer valorTasa = repositorioProducto.getInstancia().obtenerValorIndicador((String) jTableVenta.getValueAt(i, 0), modelo);
            pre = jTableVenta.getValueAt(i, 2).toString();
            can = jTableVenta.getValueAt(i, 3).toString();
            precio = Integer.parseInt(pre);
            cantidad = Integer.parseInt(can);
            total = (precio * cantidad);

            iva = (total * valorTasa / 100);
            subtotal = total - iva;

            jTableVenta.setValueAt(subtotal, i, 4);
            jTableVenta.setValueAt(iva, i, 5);
            jTableVenta.setValueAt(total, i, 6);
            sub = sub + subtotal;
            iv = iv + iva;
            tot = tot + total;
            VentanaVenta.txtSubTotal.setText("" + Math.round(sub * 100) / 100);
            VentanaVenta.txtIV.setText("" + Math.round(iv));
            VentanaVenta.txttotal.setText("" + Math.round(tot));

        }

        txtCodProducto.setText("");
        txtProducto.setText("");
        txtPrecioProducto.setText("");
        txtCant.setText("");

    }
